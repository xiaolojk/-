# Build merged arrays
all_chars = sorted(set(unicodes + list(NEW_CHARS.keys())))
print(f"New total: {len(all_chars)} characters")

# Generate new unicodes array
unicodes_lines = []
for i in range(0, len(all_chars), 16):
    chunk = all_chars[i:i+16]
    hex_str = ", ".join(f"0x{c:04X}" for c in chunk)
    unicodes_lines.append(f"    {hex_str},")

# Generate new bitmaps array
bitmap_lines = []
for c in all_chars:
    if c in NEW_CHARS:
        rows = NEW_CHARS[c]
    else:
        idx = unicodes.index(c)
        rows = bitmaps[idx]
    hex_str = ", ".join(f"0x{v:04X}" for v in rows)
    bitmap_lines.append(f"    {{{hex_str}}},")

# Replace in content
# Update FONT_CHAR_COUNT
content = re.sub(r'static const int FONT_CHAR_COUNT = \d+;', f'static const int FONT_CHAR_COUNT = {len(all_chars)};', content)

# Update FONT_UNICODES
old_uni = content.split('FONT_UNICODES[FONT_CHAR_COUNT]')[1].split('};')[0]
new_uni = '\n'.join(unicodes_lines)
content = content.replace(old_uni, '\n' + new_uni + '\n')

# Update FONT_BITMAPS
old_bmp = content.split('FONT_BITMAPS[FONT_CHAR_COUNT][16]')[1].split('};')[0]
new_bmp = '\n'.join(bitmap_lines)
content = content.replace(old_bmp, '\n' + new_bmp + '\n')

with open(FONT_H, 'w') as f:
    f.write(content)

print(f"Patched {FONT_H}")
print(f"Added {len(NEW_CHARS)} new characters: {''.join(chr(c) for c in sorted(NEW_CHARS.keys()))}")