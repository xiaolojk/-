#!/usr/bin/env python3
"""Patch font_data.h to add missing characters for key customization UI."""
import re, os

FONT_H = os.path.join(os.path.dirname(__file__), "..", "jni", "font_data.h")

# Read font_data.h
with open(FONT_H, 'r') as f:
    content = f.read()

# Extract current unicodes
unicodes_str = content.split('FONT_UNICODES')[1].split('};')[0]
unicodes = [int(u, 16) for u in re.findall(r'0x([0-9A-F]{4})', unicodes_str)]

# Extract current bitmaps
bitmaps_str = content.split('FONT_BITMAPS')[1].split('};')[0]
# Parse each character's bitmap as 16 uint16 values
bitmap_chars = re.findall(r'\{([^}]+)\}', bitmaps_str)
bitmaps = []
for bc in bitmap_chars:
    rows = [int(r.strip(), 16) for r in re.findall(r'0x([0-9A-F]{4})', bc)]
    bitmaps.append(rows)

print(f"Current: {len(unicodes)} unicodes, {len(bitmaps)} bitmaps")

